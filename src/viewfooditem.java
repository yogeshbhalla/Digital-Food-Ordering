    BufferedImage resizephoto(BufferedImage image, int width, int height) {
        BufferedImage bi = new BufferedImage(width, height, BufferedImage.TRANSLUCENT);
        Graphics2D g2d = (Graphics2D) bi.createGraphics();
        g2d.addRenderingHints(new RenderingHints(RenderingHints.KEY_RENDERING, RenderingHints.VALUE_RENDER_QUALITY));
        g2d.drawImage(image, 0, 0, width, height, null);
        g2d.dispose();
        return bi;
    }
    private void btdelete1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btdelete1ActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            int index = jTable1.getSelectedRow();
            if (index == -1) {
                JOptionPane.showMessageDialog(this, "Please select a category first");
            } else {
                int showConfirmDialog = JOptionPane.showConfirmDialog(rootPane, "Are you sure?", "Delete Confirmation", JOptionPane.OK_CANCEL_OPTION);
                if (showConfirmDialog == JOptionPane.OK_OPTION) {
                    String category = cbfoodcategory.getSelectedItem().toString();
                    String itemname = al.get(index).itemname;
                    HttpResponse<String> res = Unirest.get("http://localhost:8888/deletefooditem")
                            .queryString("category", category)
                            .queryString("itemname",itemname).asString();
                    String response = res.getBody();
                    if (response.equals("success")) {
                        JOptionPane.showMessageDialog(rootPane, "fooditem Deleted");
                        loaddata();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Deletion Failed!!!");
                    }
                }
            }
        } catch (UnirestException ex) {
            Logger.getLogger(viewcategory.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btdelete1ActionPerformed


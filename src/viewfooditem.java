    void loaddata() {
        try {
            // TODO add your handling code here:
            String category = cbfoodcategory.getSelectedItem().toString();
            HttpResponse<String> res = Unirest.get("http://localhost:8888/getfooditems")
                    .queryString("category", category).asString();
            String response = res.getBody();
            StringTokenizer st = new StringTokenizer(response, "~!@");
            int n = st.countTokens();
            al.clear();
            for (int i = 1; i <= n; i++) {
                String row = st.nextToken();
                StringTokenizer st2 = new StringTokenizer(row, "#$%");
                String itemname = st2.nextToken();
                String desc = st2.nextToken();
                String type = st2.nextToken();
                String price = st2.nextToken();
                String photo = st2.nextToken();
                fooditem obj = new fooditem(itemname, desc, type, price, photo);
                al.add(obj);
            }
            jTable1.setRowHeight(100);
            jTable1.setShowGrid(true);
            jTable1.getColumnModel().getColumn(4).setCellRenderer(new ImageRenderer());

        } catch (UnirestException ex) {
            Logger.getLogger(viewcategory.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

